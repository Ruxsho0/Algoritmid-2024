# Ülesanne 5: Graafide Värvimise Probleem

# Graafide värvimise probleem (Graph Coloring Problem) on ülesanne, kus tuleb määrata graafi tippudele värvid nii, et üksteisega ühendatud tipud ei oleks sama värvi. 
# Probleemi eesmärk on kasutada võimalikult vähe erinevaid värve.

# Graafide värvimise probleem on oluline, kuna see on seotud paljude praktiliste ülesannetega, nagu näiteks:
# 1. Ressursside jaotamine: Näiteks eksamite ajakava koostamine, kus eksamid, millel on ühised õpilased, ei tohi toimuda samal ajal.
# 2. Konfliktide lahendamine: Näiteks raadiosageduste jaotamine, kus lähedal asuvad saatjad ei tohi kasutada sama sagedust, et vältida häireid.